/**
 * Audit: recurring events whose monthly/yearly pattern is missing dayOfMonth.
 *
 * These are the records that fail to publish with the Graph error
 *   "Your request can't be completed. DayOfMonth should be between 1 and 31."
 * (the master create rolls back and the event stays pending).
 *
 * The buildGraphRecurrence fix derives dayOfMonth from range.startDate, so the
 * 500 is resolved for ALL of these. But that fix alone is only *correct* when
 * the pattern produces a single occurrence in its range. If the range spans
 * multiple periods, the (degenerate) pattern silently generates extra dates the
 * requester may not have intended — so those need a human look.
 *
 * This script is STRICTLY READ-ONLY. It classifies each affected record:
 *   [A] safe         pattern yields exactly 1 date in range  -> code fix alone is enough
 *   [B] recurrence   pattern yields >1 date, NO additions    -> genuine cadence, fix is enough
 *   [C] review       pattern yields >1 date WITH additions   -> likely over-generates (phantom dates)
 *
 * Usage:
 *   cd backend
 *   node audit-recurrence-missing-dayofmonth.js            # audit all non-deleted events
 *   node audit-recurrence-missing-dayofmonth.js --pending  # only pending/draft (publishable) events
 */

const { MongoClient } = require('mongodb');
// Load .env from this script's directory so the audit can be run from any cwd
// (e.g. `node backend/audit-...js` from the repo root, or from within backend/).
require('dotenv').config({ path: require('path').join(__dirname, '.env') });

const { expandAllOccurrences } = require('./utils/recurrenceExpansion');

const MONGODB_URI = process.env.MONGODB_CONNECTION_STRING || 'mongodb://localhost:27017';
const DB_NAME = process.env.MONGODB_DATABASE_NAME || 'emanuelnyc';
const COLLECTION = 'templeEvents__Events';

const PENDING_ONLY = process.argv.includes('--pending');
const PUBLISHABLE_STATUSES = ['pending', 'draft', 'room-reservation-request'];

const MONTHLY_YEARLY = new Set(['monthly', 'yearly', 'absoluteMonthly', 'absoluteYearly']);

function missingDayOfMonth(pattern) {
  if (!pattern || !MONTHLY_YEARLY.has(pattern.type)) return false;
  const d = pattern.dayOfMonth;
  return typeof d !== 'number' || d < 1 || d > 31;
}

function startTimePartOf(ev) {
  const dt = ev.calendarData?.startDateTime || ev.startDateTime || '';
  return typeof dt === 'string' && dt.includes('T') ? dt : `${ev.calendarData?.startDate || ''}T12:00:00`;
}

async function main() {
  const client = new MongoClient(MONGODB_URI);
  await client.connect();
  const db = client.db(DB_NAME);
  const col = db.collection(COLLECTION);

  console.log('Audit: recurring monthly/yearly events missing pattern.dayOfMonth');
  console.log(`  DB: ${DB_NAME}  Collection: ${COLLECTION}`);
  console.log(`  Scope: ${PENDING_ONLY ? 'pending/draft only' : 'all non-deleted events'}`);
  console.log('');

  const query = {
    isDeleted: { $ne: true },
    'recurrence.pattern.type': { $in: ['monthly', 'yearly', 'absoluteMonthly', 'absoluteYearly'] },
  };
  if (PENDING_ONLY) query.status = { $in: PUBLISHABLE_STATUSES };

  const candidates = await col
    .find(query)
    .project({
      eventId: 1, status: 1, recurrence: 1,
      'calendarData.eventTitle': 1, 'calendarData.startDateTime': 1,
      'calendarData.endDateTime': 1, 'calendarData.startDate': 1,
      startDateTime: 1, endDateTime: 1, requesterEmail: 1, graphEventId: 1,
    })
    .toArray();

  const affected = candidates.filter(ev => missingDayOfMonth(ev.recurrence?.pattern));

  const classes = { A: [], B: [], C: [] };

  for (const ev of affected) {
    const rec = ev.recurrence;
    const startTime = startTimePartOf(ev);
    const endTime = ev.calendarData?.endDateTime || ev.endDateTime || startTime;

    // Pattern-only occurrences (strip additions/exclusions) = what Graph will
    // generate once dayOfMonth is derived from startDate.
    const patternOnly = expandAllOccurrences(
      { ...rec, additions: [], exclusions: [] }, startTime, endTime
    ).map(o => o.occurrenceDate);

    const additions = rec.additions || [];
    const start = rec.range?.startDate;

    // "Phantom" pattern dates: generated by the pattern but neither the anchor
    // (start) nor an explicitly chosen addition.
    const phantom = patternOnly.filter(d => d !== start && !additions.includes(d));

    const row = {
      eventId: ev.eventId,
      status: ev.status,
      published: !!ev.graphEventId,
      title: ev.calendarData?.eventTitle || '(untitled)',
      requester: ev.requesterEmail || '',
      type: rec.pattern.type,
      range: `${rec.range?.startDate} -> ${rec.range?.endDate || rec.range?.type}`,
      patternCount: patternOnly.length,
      additions,
      phantom,
    };

    if (patternOnly.length <= 1) classes.A.push(row);
    else if (additions.length === 0) classes.B.push(row);
    else classes.C.push(row);
  }

  const printRow = (r, showPhantom) => {
    console.log(`  - ${r.title}`);
    console.log(`      eventId=${r.eventId}  status=${r.status}${r.published ? ' (on Graph)' : ''}`);
    console.log(`      ${r.type}  range ${r.range}  patternDates=${r.patternCount}  additions=${r.additions.length}`);
    if (showPhantom && r.phantom.length) {
      console.log(`      >>> phantom pattern dates (likely unintended): ${r.phantom.join(', ')}`);
    }
  };

  console.log(`Found ${affected.length} affected record(s).`);
  console.log('');
  console.log(`[A] Safe — single pattern occurrence, code fix alone resolves (${classes.A.length}):`);
  classes.A.forEach(r => printRow(r, false));
  console.log('');
  console.log(`[B] Genuine recurrence — multi-occurrence, no additions, code fix resolves (${classes.B.length}):`);
  classes.B.forEach(r => printRow(r, false));
  console.log('');
  console.log(`[C] REVIEW — pattern over-generates alongside ad-hoc additions (${classes.C.length}):`);
  classes.C.forEach(r => printRow(r, true));
  console.log('');
  console.log('Summary:');
  console.log(`  [A] safe:       ${classes.A.length}`);
  console.log(`  [B] recurrence: ${classes.B.length}`);
  console.log(`  [C] review:     ${classes.C.length}  <-- need data correction before/after publish`);
  console.log(`  total affected: ${affected.length}`);

  await client.close();
}

main().catch(err => {
  console.error('Audit failed:', err);
  process.exit(1);
});
